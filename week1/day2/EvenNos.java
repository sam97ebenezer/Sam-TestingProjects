package week1.day2;

public class EvenNos {

	public static void main(String[] args) {
		// Using for loop to iterate from 1 to 10
        System.out.println("Even numbers from 1 to 10:");
        for (int i = 1; i <= 10; i++) {
            // Check if the number is even
            if (i % 2 == 0) {
                System.out.println(i); // Print the even number
            }
        }
	}
}
        
